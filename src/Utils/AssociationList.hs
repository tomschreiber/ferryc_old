module Utils.AssociationList(

-- =============================================================================
-- exports
-- =============================================================================

    keys, 
    diff, 
    retainByKeys

) where

-- =============================================================================
-- imports
-- =============================================================================

import Data.List((\\))


-- =============================================================================
-- functions
-- =============================================================================


----------------------------------------------------------------------
----------------------------------------------------------------------
-- returns all keys of an association list
keys :: [(a,b)] -> [a]
----------------------------------------------------------------------
----------------------------------------------------------------------

keys = map fst


----------------------------------------------------------------------
----------------------------------------------------------------------
-- list difference
diff :: Eq a => [a] -> [a] -> [a]
----------------------------------------------------------------------
----------------------------------------------------------------------

diff = (\\)


----------------------------------------------------------------------
----------------------------------------------------------------------
-- retains only those mappings for which a key exists in the second argument
retainByKeys :: Eq a => [(a,b)] -> [a] -> [(a,b)]
----------------------------------------------------------------------
----------------------------------------------------------------------

retainByKeys ms ks = filter (\ (k,_v) -> (elem k ks)) ms 
